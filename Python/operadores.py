# > Operações Matemáticas/Aritméticas

"""
	- Soma: +
	- Subtração: -
	- Multiplicação: *
	- Divisão: /
	- Divisão Inteira: //
	- Resto da divisão: %
	- Potência: **
"""

numero1 = 5
numero2 = 10

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)	# na divisão inteira, os decimais são descartados, logo não há números quebrados
print(numero1 % numero2)	# 
print(numero1 ** numero2)

# > Operadores Booleanos

idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2)				# false
print(idade1 <= idade2)			# true
print('Python' == 'python')		# false
print('banana' != 'abacaxi')		# true
print(altura1 >= altura2)			# true
print(altura2 = altura3)			# false