# 1. Variáveis

idade = 26 # tipo int, idade recebe o que vier ao fim da mesma, no caso "26"

print (idade)

nome = 'Guilherme Santoro' # tipo str, texto

print (nome)

"""
Tipos de variáveis
1. int: números inteiros, ou seja, números sem parte decimal: 0, 5, -1, 1000
2. float: números reias, ou seja, números com parte decimal: 1.0, -5.8, 2.54
3. str: cadeia de caracteres, ou seja, dados textuais
4. bool: valores lógicos (booleanos): True ou False
"""

altura = 1.74 # tipo float
estudando = False

# No Python há diferenciação entre minisculos e maiusculos (case sensitive)
# Ou seja, alguns recursos podem ou não funcionar dependendo de como são digitados.

print ( type(idade) )
print ( type(altura) )
print ( type(estudando) )
print ( type(nome) )


